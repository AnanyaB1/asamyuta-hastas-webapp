def draw_info_text(image, brect, handedness, hand_sign_text):
    #cv.rectangle(image, (brect[0], brect[1]), (brect[2], brect[1] - 22),
    #             (0, 0, 0), -1)

    info_text = handedness.classification[0].label[0:]
    if hand_sign_text != "":
        info_text = info_text + ':' + hand_sign_text
    
    text_size = cv.getTextSize(info_text, cv.FONT_HERSHEY_SIMPLEX, 0.6, cv.LINE_AA)[0]
    box_width = text_size[0] + 10  # Add some padding to prevent the text from touching box edges
    box_height = text_size[1] + 10

    # Draw the background box
    cv.rectangle(image, (brect[0], brect[1] - box_height), (brect[2] + box_width, brect[1]), (0,0,0), -1)

    
    cv.putText(image, info_text, (brect[0] + 5, brect[1] - 4),
               cv.FONT_HERSHEY_SIMPLEX, 0.6, (255, 255, 255), 1, cv.LINE_AA)


    return image
